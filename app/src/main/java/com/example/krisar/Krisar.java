public class Krisar {
    public String nados;
    public String matkul;
    public String kelas;
    public String krisar;

    public Krisar() {

    }

    public Krisar(String nados, String matkul, String kelas, String krisar) {
        this.nados = nados;
        this.matkul = matkul;
        this.kelas = kelas;
        this.krisar = krisar;
    }
}
